//Write A Program to represent Working Bitwise operators
 #include <stdio.h>
 int main()
{
int a = 12, b = 25;
printf("a&b = %d", a&b);
printf("a|b = %d\n", a|b);
printf("a^b= %d\n", a^b);
printf("Complement of a = %d\n",~a);
printf("Complement of b = %d\n",~b);
printf("a<<2 = %d\n",a<<2);
printf("b<<1 = %d\n",b<<1);
printf("a>>2 = %d\n",a>>2);
printf("b>>1 = %d\n",b>>1);
}
