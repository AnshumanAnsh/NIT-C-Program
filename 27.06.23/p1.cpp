//WAP Of Arithmetic operators
#include<stdio.h>
main()
{
	int a=21,b=10,c;
	c=a+b;
	printf("value of c is %d\n",c);
	c=a-b;
	printf("value of c is %d\n",c);
	c=a*b;
	printf("value of c is %d\n",c);
	c=a/b;
	printf("value of c is %d\n",c);
	c=a%b;
	printf("value of c is %d\n",c);
	c=++a;
	printf("value of c is %d\n",c);
	c=a--;
	printf("value of c is %d\n",c);
	
}
